/*Question 1*/
$(".buttons1").click(function() {
    if ($("#q1").hasClass("hidden")) {
      $("#q1").removeClass("hidden");
      $("#q1-default").addClass("hidden");
    } else {
      $("#q1").addClass("hidden");
      $("#q1-default").removeClass("hidden");
    }
    if ($("#ans1").hasClass("hidden")) {
      $("#ans1").removeClass("hidden");
    } else {
      $("#ans1").addClass("hidden");
    }
  });

